public class mul_table {
  public static void main(String[] args) {
    int num = 5;
    System.out.println("The Multiplication table of" +num+  "is: ");
    for ( int i = 1; i<=10; i++){
        int result = num * i;
        System.out.println(num+ " x " + i + " = " + result);
        
    }

  }
}
