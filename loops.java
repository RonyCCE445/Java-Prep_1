public class loops {
    public static void main(String[] args) {
        int i =0;
         while (i<=5){
            System.out.println(i);
            i++;
         }
            for (int j=5; j<=10; j++){
                System.out.println(j);
            }

            do {
                System.out.println(i);
                i++;
            } while (i<=20);
    } 


}
